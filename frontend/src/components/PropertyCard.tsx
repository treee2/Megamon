import { useState } from 'react';
import type { Property } from '../types';
import './PropertyCard.css';

interface Props {
  property: Property;
}

const PropertyCard = ({ property }: Props) => {
  const [currentImage, setCurrentImage] = useState(0);
  const [showBooking, setShowBooking] = useState(false);
  const [checkIn, setCheckIn] = useState('');
  const [checkOut, setCheckOut] = useState('');

  const handleBook = async () => {
    if (!checkIn || !checkOut) {
      alert('–í—ã–±–µ—Ä–∏—Ç–µ –¥–∞—Ç—ã –∑–∞–µ–∑–¥–∞ –∏ –≤—ã–µ–∑–¥–∞');
      return;
    }

    try {
      const webApp = window.Telegram?.WebApp;
      const user = webApp?.initDataUnsafe?.user;

      const response = await fetch('http://localhost:3000/api/bookings', {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({
          telegramId: user?.id?.toString() || 'test_user',
          firstName: user?.first_name || '–¢–µ—Å—Ç',
          username: user?.username,
          propertyId: property.id,
          checkIn,
          checkOut
        })
      });

      if (response.ok) {
        alert('–ë—Ä–æ–Ω–∏—Ä–æ–≤–∞–Ω–∏–µ —É—Å–ø–µ—à–Ω–æ —Å–æ–∑–¥–∞–Ω–æ!');
        setShowBooking(false);
        setCheckIn('');
        setCheckOut('');
      }
    } catch {
      alert('–û—à–∏–±–∫–∞ –±—Ä–æ–Ω–∏—Ä–æ–≤–∞–Ω–∏—è');
    }
  };

  const nextImage = (e: React.MouseEvent) => {
    e.stopPropagation();
    setCurrentImage((prev) => (prev + 1) % property.images.length);
  };

  const prevImage = (e: React.MouseEvent) => {
    e.stopPropagation();
    setCurrentImage((prev) => 
      prev === 0 ? property.images.length - 1 : prev - 1
    );
  };

  return (
    <div className="property-card">
      <div className="image-container">
        <img src={property.images[currentImage]} alt={property.title} />
        
        {property.images.length > 1 && (
          <>
            <button className="img-nav left" onClick={prevImage}>‚Äπ</button>
            <button className="img-nav right" onClick={nextImage}>‚Ä∫</button>
            <div className="image-dots">
              {property.images.map((_, idx) => (
                <span 
                  key={idx} 
                  className={idx === currentImage ? 'active' : ''}
                />
              ))}
            </div>
          </>
        )}
      </div>

      <div className="property-info">
        <h2>{property.title}</h2>
        <p className="price">{property.price.toLocaleString()} ‚ÇΩ / –Ω–æ—á—å</p>
        
        <div className="details">
          <span>üõè {property.rooms} –∫–æ–º–Ω.</span>
          <span>üìê {property.area} –º¬≤</span>
        </div>

        <p className="address">üìç {property.address}</p>
        <p className="description">{property.description}</p>

        {!showBooking ? (
          <button className="book-btn" onClick={() => setShowBooking(true)}>
            –ó–∞–±—Ä–æ–Ω–∏—Ä–æ–≤–∞—Ç—å
          </button>
        ) : (
          <div className="booking-form">
            <input
              type="date"
              value={checkIn}
              onChange={(e) => setCheckIn(e.target.value)}
              min={new Date().toISOString().split('T')[0]}
              placeholder="–ó–∞–µ–∑–¥"
            />
            <input
              type="date"
              value={checkOut}
              onChange={(e) => setCheckOut(e.target.value)}
              min={checkIn}
              placeholder="–í—ã–µ–∑–¥"
            />
            <div className="booking-actions">
              <button onClick={() => setShowBooking(false)}>–û—Ç–º–µ–Ω–∞</button>
              <button onClick={handleBook} className="confirm-btn">
                –ü–æ–¥—Ç–≤–µ—Ä–¥–∏—Ç—å
              </button>
            </div>
          </div>
        )}
      </div>
    </div>
  );
};

export default PropertyCard;